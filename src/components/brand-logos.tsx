export function ProviderLogo({ type, className }: { type: string, className?: string }) {
  const props = { className: cn("size-5", className) };
  
  switch (type.toUpperCase()) {
    case "OPENAI":
      return <OpenAILogo {...props} />;
    case "ANTHROPIC":
      return <AnthropicLogo {...props} />;
    case "DEEPSEEK":
      return <DeepSeekLogo {...props} />;
    case "GOOGLE_SHEETS":
    case "GOOGLE_SPREADSHEETS":
      return <GoogleSheetsLogo {...props} />;
    case "ELEVENLABS":
      return <ElevenLabsLogo {...props} />;
    case "NOCODB":
      return <NocoDBLogo {...props} />;
    case "WHATSAPP":
    case "WHATSAPP_CLOUD":
      return <WhatsAppLogo {...props} className={cn(props.className, "text-[#25D366]")} />;
    default:
      return <Globe {...props} className={cn(props.className, "text-muted-foreground")} />;
  }
}
